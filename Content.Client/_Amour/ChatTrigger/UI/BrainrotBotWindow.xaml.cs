using Content.Client.UserInterface.Controls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client._Amour.ChatTrigger.UI;

[GenerateTypedNameReferences]
public sealed partial class BrainrotBotWindow : FancyWindow
{
    public Action? OnSendAnyway;

    public BrainrotBotWindow()
    {
        RobustXamlLoader.Load(this);
        MessageLabel.SetMessage(FormattedMessage.FromMarkupPermissive(Loc.GetString("chat-trigger-brainrot-message")));
        CancelButton.OnPressed += _ => Close();
        SendAnywayButton.OnPressed += _ =>
        {
            OnSendAnyway?.Invoke();
            Close();
        };
    }

    public void SetDescription(string description)
    {
        DescriptionLabel.SetMessage(FormattedMessage.FromMarkupPermissive(description));
    }

    public void SetTriggerWord(string trigger)
    {
        var msg = new FormattedMessage();
        msg.AddText(Loc.GetString("chat-trigger-brainrot-trigger-prefix"));
        msg.PushColor(Color.OrangeRed);
        msg.AddText($" «{trigger}»");
        msg.Pop();
        TriggerLabel.SetMessage(msg);
    }
}
